class MyError(Exception):   # 覆盖Exception中的__init__()方法
    def __init__(self,value,message):
        self.value=value
        self.message=message

try:
    raise MyError("404","not found")
except MyError as e:    #e是个对象，value和message是属性
    print("创建error:{}--{}".format(e.value,e.message))


